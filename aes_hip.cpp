const uint8_t Rcon[11] = { 0x8d, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36 };

void KeyExpansion_Host(const uint8_t* Key, uint8_t* RoundKey) {
    uint32_t i, j, k;
    uint8_t tempa[4];
    
    for (i = 0; i < Nk; ++i) {
        RoundKey[(i * 4) + 0] = Key[(i * 4) + 0];
        RoundKey[(i * 4) + 1] = Key[(i * 4) + 1];
        RoundKey[(i * 4) + 2] = Key[(i * 4) + 2];
        RoundKey[(i * 4) + 3] = Key[(i * 4) + 3];
    }
    for (i = Nk; i < Nb * (Nr + 1); ++i) {
        k = (i - 1) * 4;
        tempa[0] = RoundKey[k + 0]; tempa[1] = RoundKey[k + 1];
        tempa[2] = RoundKey[k + 2]; tempa[3] = RoundKey[k + 3];
        if (i % Nk == 0) {
            uint8_t u8tmp = tempa[0];
            // BUG CORRIGIDO: Usa sbox_host em vez de d_sbox
            tempa[0] = sbox_host[tempa[1]]; 
            tempa[1] = sbox_host[tempa[2]];
            tempa[2] = sbox_host[tempa[3]]; 
            tempa[3] = sbox_host[u8tmp];
            tempa[0] = tempa[0] ^ Rcon[i / Nk];
        }
        j = i * 4; k = (i - Nk) * 4;
        RoundKey[j + 0] = RoundKey[k + 0] ^ tempa[0];
        RoundKey[j + 1] = RoundKey[k + 1] ^ tempa[1];
        RoundKey[j + 2] = RoundKey[k + 2] ^ tempa[2];
        RoundKey[j + 3] = RoundKey[k + 3] ^ tempa[3];
    }
}


int main(int argc, char* argv[]) {
    if (argc < 3) {
        std::cout << "Uso: ./aes_hip <-e ou -d> <arquivo_entrada>" << std::endl;
        return 1;
    }

    std::string mode = argv[1];
    std::string filename = argv[2];
    
    std::ifstream file(filename, std::ios::binary);
    if (!file) {
        std::cerr << "Erro ao abrir arquivo!" << std::endl;
        return 1;
    }

    std::vector<uint8_t> data((std::istreambuf_iterator<char>(file)), std::istreambuf_iterator<char>());
    file.close();

    // Aplica o padding se for criptografar
    if (mode == "-e") {
        size_t original_size = data.size();
        uint8_t padding = 16 - (original_size % 16);
        for (int i = 0; i < padding; ++i) data.push_back(padding);
    }

    uint8_t key[16] = {0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6, 0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c};
    uint8_t RoundKey_Host[176];

    // Gera as sub-chaves na CPU antes de enviar para a GPU
    KeyExpansion_Host(key, RoundKey_Host);

    // Variáveis da GPU
    uint8_t *d_data, *d_RoundKey;
    size_t data_size = data.size();
    size_t num_blocks = data_size / 16;

    // Alocações de memória na Placa de Vídeo
    hipMalloc((void**)&d_data, data_size);
    hipMalloc((void**)&d_RoundKey, 176);

    // INÍCIO DO CRONÔMETRO
    auto start = std::chrono::high_resolution_clock::now();

    // Copia os dados do arquivo e as sub-chaves para a GPU
    hipMemcpy(d_data, data.data(), data_size, hipMemcpyHostToDevice);
    hipMemcpy(d_RoundKey, RoundKey_Host, 176, hipMemcpyHostToDevice);

    // Malha de execução
    int threadsPerBlock = 256;
    int blocksPerGrid = (num_blocks + threadsPerBlock - 1) / threadsPerBlock;

    // Dispara o kernel correspondente à linha de comando
    if (mode == "-e") {
        hipLaunchKernelGGL(aes_encrypt_kernel, dim3(blocksPerGrid), dim3(threadsPerBlock), 0, 0, d_data, d_RoundKey, num_blocks);
    } else if (mode == "-d") {
        hipLaunchKernelGGL(aes_decrypt_kernel, dim3(blocksPerGrid), dim3(threadsPerBlock), 0, 0, d_data, d_RoundKey, num_blocks);
    } else {
        std::cerr << "Modo invalido. Use -e ou -d." << std::endl;
        hipFree(d_data);
        hipFree(d_RoundKey);
        return 1;
    }

    hipDeviceSynchronize();
    
    // Copia os dados prontos da GPU de volta para a Memória RAM
    hipMemcpy(data.data(), d_data, data_size, hipMemcpyDeviceToHost);

    // FIM DO CRONÔMETRO
    auto end = std::chrono::high_resolution_clock::now();
    
    // Liberação de memória
    hipFree(d_data);
    hipFree(d_RoundKey);

    // Remove o padding se for descriptografar
    if (mode == "-d" && !data.empty()) {
        uint8_t pad_val = data.back();
        if (pad_val > 0 && pad_val <= 16) {
            data.resize(data.size() - pad_val);
        }
    }

    // Salva o arquivo modificado no computador
    std::string out_filename = (mode == "-e") ? filename + ".enc" : filename + ".dec";
    std::ofstream outfile(out_filename, std::ios::binary);
    outfile.write(reinterpret_cast<const char*>(data.data()), data.size());
    outfile.close();

    // Cálculos estatísticos
    std::chrono::duration<double> elapsed = end - start;
    double tempo_segundos = elapsed.count();
    double tamanho_mb = data.size() / (1024.0 * 1024.0);
    double throughput = tamanho_mb / tempo_segundos;

    // Impressão dos resultados
    std::cout << std::fixed << std::setprecision(6);
    std::cout << "--- Resultados Benchmark HIP ---" << std::endl;
    std::cout << "Modo: " << (mode == "-e" ? "Criptografia" : "Descriptografia") << std::endl;
    std::cout << "Arquivo Entrada: " << filename << std::endl;
    std::cout << "Arquivo Saida: " << out_filename << std::endl;
    std::cout << "Tempo de Execucao: " << tempo_segundos << " s" << std::endl;
    std::cout << "Throughput: " << throughput << " MB/s" << std::endl;

    return 0;
}